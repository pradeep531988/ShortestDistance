CREATE TABLE node (
  id int NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY
        (START WITH 1, INCREMENT BY 1),
  name varchar(30) NOT NULL UNIQUE);

CREATE TABLE paths (
     id int NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY
        (START WITH 1, INCREMENT BY 1),
     from_node varchar(30) NOT NULL REFERENCES node(name)ON DELETE CASCADE ON UPDATE RESTRICT,
     to_node varchar(30) NOT NULL REFERENCES node(name)ON DELETE CASCADE ON UPDATE RESTRICT,
     weight int NOT NULL
);

SELECT * FROM node;